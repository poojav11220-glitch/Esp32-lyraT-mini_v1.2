static void oled_draw_char(uint8_t page, uint8_t col, char c)
{
    if (c < 32 || c > 122) c = ' ';
    oled_set_cursor(page, col);
    uint8_t glyph[6];
    memcpy(glyph, font5x7[c - 32], 5);
    glyph[5] = 0x00;
    oled_send_data(glyph, 6);
}

static void oled_draw_string(uint8_t page, uint8_t col, const char *str)
{
    while (*str) {
        oled_draw_char(page, col, *str++);
        col += 6;
        if (col > 122) break;
    }
}

/* ── Main ── */
void app_main(void)
{
    soft_i2c_init();
    vTaskDelay(pdMS_TO_TICKS(100));   // let OLED power rail settle

    oled_init();
    oled_clear();

    oled_draw_string(0, 0, "Hello World!");
    oled_draw_string(2, 0, "ESP32 LyraT");
    oled_draw_string(3, 0, "Counter:");

    int counter = 0;
    char buf[20];

    while (1) {
        snprintf(buf, sizeof(buf), "%d   ", counter);
        oled_draw_string(4, 0, buf);
        printf("Counter: %d\n", counter);
        counter++;
        vTaskDelay(pdMS_TO_TICKS(1000));
    }
}
